nome = input('digite seu nome ')
print('Prazer em te conhecer {:20}'.format(nome)) #escreve o nome em 20 espaços
print('Prazer em te conhecer {:<20}'.format(nome)) #formata a esquerda em 20 espaços
print('Prazer em te conhecer {:>20}'.format(nome)) #formata a direita em 20 espaços
print('Prazer em te conhecer {:^20}'.format(nome)) #centraliza com 20 espaços
print('prazer em te conhecer {:=^20}'.format(nome)) #centraliza e adiciona = nos 20 espaços
